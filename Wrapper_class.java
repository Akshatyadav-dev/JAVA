public class Wrapper_class {
    public static void main(String[] args) {
        Integer integer = new Integer (10);  // Using wrapper class
        System.out.println("Wrapper Class : " + integer);

        // Autoboxing (Java 5 and later)
        Integer autoBoxed = 10;
        System.out.println("Auto-Boxed Integer : " + autoBoxed);
    }
    
}
